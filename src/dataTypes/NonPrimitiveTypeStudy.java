package dataTypes;

public class NonPrimitiveTypeStudy {

	public static void main(String[] args) {
		// I want to store CityName
		
		//1 Variable declaration
		
		String CityName;
		
		//2 assign value
		CityName="Pune";
		
		//3 usage
		System.out.println("My CityName is"+CityName);
		
		
		// I want to store my best friend name
		
		//1 Variable declaration
		
		String myfriend;
		
		//2 assign value
		myfriend="Swapnali Thorat";
		
		//3 usage
		System.out.println("My Best friend name is"+myfriend);

	}

}
